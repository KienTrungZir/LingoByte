def seed():
    conn = pyodbc.connect(CONN_STR)
    cursor = conn.cursor()
    
    # Get existing chars and radicals
    cursor.execute("SELECT hanzi, char_id FROM Characters")
    existing = {row.hanzi.strip(): row.char_id for row in cursor.fetchall()}
    
    cursor.execute("SELECT character, radical_id FROM Radicals")
    rad_map = {row.character.strip(): row.radical_id for row in cursor.fetchall()}
    
    inserted = 0
    linked = 0
    
    for hanzi, pinyin, meaning, hsk, rads in CHARS:
        if hanzi in existing:
            char_id = existing[hanzi]
            # Update meaning_vi if empty
            cursor.execute("UPDATE Characters SET meaning_vi=COALESCE(NULLIF(meaning_vi,''), ?), pinyin=COALESCE(NULLIF(pinyin,''), ?), hsk_level=COALESCE(hsk_level, ?) WHERE char_id=?",
                          (meaning, pinyin, hsk, char_id))
        else:
            cursor.execute(
                "INSERT INTO Characters (hanzi, pinyin, meaning_vi, hsk_level) VALUES (?,?,?,?)",
                (hanzi, pinyin, meaning, hsk)
            )
            char_id = cursor.execute("SELECT @@IDENTITY").fetchone()[0]
            existing[hanzi] = char_id
            inserted += 1
        
        # Create radical links
        for rad_char in rads:
            rid = rad_map.get(rad_char)
            if rid:
                exists = cursor.execute(
                    "SELECT 1 FROM CharacterRadicalRel WHERE char_id=? AND radical_id=?",
                    (char_id, rid)
                ).fetchone()
                if not exists:
                    try:
                        cursor.execute("INSERT INTO CharacterRadicalRel (char_id, radical_id) VALUES (?,?)", (char_id, rid))
                        linked += 1
                    except: pass
    
    conn.commit()
    print(f"Inserted: {inserted} chars, Created: {linked} links")
    cursor.execute("SELECT COUNT(*) FROM Characters")
    print(f"Total characters: {cursor.fetchone()[0]}")
    cursor.execute("SELECT COUNT(*) FROM CharacterRadicalRel")
    print(f"Total links: {cursor.fetchone()[0]}")
    conn.close()

if __name__ == "__main__":
    seed()
